use crate::{header::Header, mbc::MBCType};

pub type MemoryError = String;

pub struct Memory {
    header: Option<Header>,
    cart: MBCType,
    errors: Vec<String>, // To store any memory-related errors

    vram: [u8; 0x2000],       // Video RAM (0x8000 - 0x9FFF)
    wram: [u8; 0x2000],       // Work RAM (0xC000 - 0xDFFF)
    oam: [u8; 0xA0],          // Object Attribute Memory (OAM) (0xFE00 - 0xFE9F)
    io_registers: [u8; 0x80], // I/O registers (0xFF00 - 0xFF7F)
    hram: [u8; 0x7F],         // High RAM (0xFF80 - 0xFFFE)
    ie_register: u8,          // Interrupt Enable Register (0xFFFF)
}

impl Memory {
    pub fn new() -> Self {
        Self {
            header: None,
            cart: MBCType::NMBC(crate::mbc::nmbc::NMBC::new(vec![0; 0x8000], 0).unwrap()),
            errors: Vec::new(),
            vram: [0; 0x2000],
            wram: [0; 0x2000],
            oam: [0; 0xA0],
            io_registers: [0; 0x80],
            hram: [0; 0x7F],
            ie_register: 0,
        }
    }

    pub fn load_rom(&mut self, rom: Vec<u8>) -> Result<(), MemoryError> {
        if rom.len() < 0x150 {
            return Err("ROM size is too small to contain a valid header".to_string());
        }

        // Guaranteed not to panic because of length check above and constant indexing
        let header = crate::header::Header::parse(&rom[0x100..0x150].try_into().unwrap())?;

        if rom.len() != header.rom_size {
            return Err(format!(
                "ROM size ({}) does not match expected the size specified by the header ({})",
                rom.len(),
                header.rom_size
            ));
        }
        return Ok(());
    }

    pub fn read_byte(&mut self, address: u16) -> u8 {
        let res = match address {
            0x0000..0x0100 if self.io_registers[0x50] == 0 => BOOT[address as usize],
            0xE000..=0xFDFF => {
                // ECHO RAM: Mirror of 0xC000-0xDFFF
                self.errors.push(format!(
                    "Attempted to read from ECHO RAM at address {:04X}",
                    address
                ));
                self.wram[address as usize - 0xE000]
            }
            0xFEA0..=0xFEFF => {
                // Unusable memory area
                self.errors.push(format!(
                    "Attempted to read from unusable memory at address {:04X}",
                    address
                ));
                0x00 // Return 0x00 for reads from unusable memory
            }
            0x8000..=0x9FFF => self.vram[address as usize - 0x8000],
            0xC000..=0xDFFF => self.wram[address as usize - 0xC000],
            0xFE00..=0xFE9F => self.oam[address as usize - 0xFE00],
            0xFF00..=0xFF7F => self.io_registers[address as usize - 0xFF00],
            0xFF80..=0xFFFE => self.hram[address as usize - 0xFF80],
            0xFFFF => self.ie_register,
            _ => self.cart.read(address),
        };
        res
    }

    #[expect(dead_code)]
    pub fn read_word(&mut self, address: u16) -> [u8; 2] {
        assert!(
            address != u16::MAX,
            "Address overflow: Cannot read a word (2 bytes) at address 0xFFFF"
        );
        [self.read_byte(address), self.read_byte(address + 1)]
    }

    pub fn write_byte(&mut self, address: u16, value: u8) {
        match address {
            0x0000..0x0100 if self.io_registers[0x50] == 0 => {
                self.errors.push(format!(
                    "Attempted to write to BOOT ROM at address {:04X} while it is still mapped",
                    address
                ));
            },
            0xE000..=0xFDFF => {
                // ECHO RAM: Mirror of 0xC000-0xDFFF
                self.errors.push(format!(
                    "Written to ECHO RAM at address {:04X}",
                    address
                ));
                self.wram[address as usize - 0xE000] = value;
            }
            0xFEA0..=0xFEFF => {
                // Unusable memory area
                self.errors.push(format!(
                    "Attempted to write to unusable memory at address {:04X}",
                    address
                ));
            }
            0x8000..=0x9FFF => self.vram[address as usize - 0x8000] = value,
            0xC000..=0xDFFF => self.wram[address as usize - 0xC000] = value,
            0xFE00..=0xFE9F => self.oam[address as usize - 0xFE00] = value,
            0xFF00..=0xFF7F => self.io_registers[address as usize - 0xFF00] = value,
            0xFF80..=0xFFFE => self.hram[address as usize - 0xFF80] = value,
            0xFFFF => self.ie_register = value,
            _ => self.cart.write(address, value),
        };
    }

    pub fn write_word(&mut self, address: u16, values: &[u8; 2]) {
        assert!(
            address != u16::MAX,
            "Address overflow: Cannot write a word (2 bytes) at address 0xFFFF"
        );
        self.write_byte(address, values[0]);
        self.write_byte(address + 1, values[1]);
    }

    pub fn take_errors(&mut self) -> Vec<String> {
        std::mem::take(&mut self.errors)
    }
}

